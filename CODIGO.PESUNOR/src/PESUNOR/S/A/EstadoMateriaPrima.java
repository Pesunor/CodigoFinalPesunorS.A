    private void TablaEstadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaEstadoMouseClicked
         try{
            int fila = TablaEstado.getSelectedRow();
            int id = Integer.parseInt(TablaEstado.getValueAt(fila, 0).toString());
             
            PreparedStatement ps;
            ResultSet rs;
            
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("select estado from LoteMateriaPrima WHERE IDLOTE = ? ");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            
            while(rs.next()){
                jIDlote.setText(String.valueOf(id));
                jEstado.setText(rs.getString("Estado"));
                
            }
     
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "HUBO UN ERROR "+e.toString());
        }
    }//GEN-LAST:event_TablaEstadoMouseClicked

