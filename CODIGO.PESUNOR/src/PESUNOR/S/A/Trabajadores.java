    private void TablaEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaEmpleadoMouseClicked
         try{
            int fila = TablaEmpleado.getSelectedRow();
            int id = Integer.parseInt(TablaEmpleado.getValueAt(fila, 0).toString());
             
            PreparedStatement ps;
            ResultSet rs;
            
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("select nombre,apellido,contraseña,areatrabajo from empleado WHERE idempleado = ? ");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            
            while(rs.next()){
                jRut.setText(String.valueOf(id));
                jNombre.setText(rs.getString("nombre"));
                jApellido.setText(rs.getString("apellido"));
                jContraseña.setText(rs.getString("contraseña"));
                jArea.setText(rs.getString("areatrabajo"));
                
            }
     
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "HUBO UN ERROR "+e.toString());
        }
    }//GEN-LAST:event_TablaEmpleadoMouseClicked

