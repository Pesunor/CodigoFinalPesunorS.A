    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int Idemp = Integer.parseInt(jRut.getText());
        String nombre = jNombre.getText();
        String apellido = jApellido.getText();
        String contraseña = jContraseña.getText();
        String area = jArea.getText();
        
        
        try{
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO empleado (idempleado,nombre,apellido,contraseña,areatrabajo)VALUES (?,?,?,?,?)");
            ps.setInt(1, Idemp);
            ps.setString(2, nombre);
            ps.setString(3, apellido);
            ps.setString(4, contraseña);
            ps.setString(5, area);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"DATOS GUARDADOS");
            limpiar();
            CargarTabla();
            
            
            
            
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, " NO SE PUEDO INGRESAR DATOS CORRECTAMENTE "+e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

