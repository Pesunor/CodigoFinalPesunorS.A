    private void MostrarDatos(){
       
        
       try{
       
        
        
        
        PreparedStatement ps;
        ResultSet rs;
        
        Connection con = Conexion.getConexion();
        ps = con.prepareStatement("SELECT * FROM LoteMateriaPrima ");
        rs = ps.executeQuery();
     
        
        while(rs.next()){
        jLote.setText(rs.getString("IDLOTE"));
        jFecha.setText(rs.getString("fecha"));
        }
        
        
   
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null,"NO MOSTRO LOS DATOS CORRECTAMENTE"+e.toString());
       };
       
       try {
        PreparedStatement ps;
        ResultSet rs;
        
        Connection con = Conexion.getConexion();
        ps = con.prepareStatement("SELECT IdLoteLatas FROM LoteLatasVacias");
        rs = ps.executeQuery();
        
        while(rs.next()){
        jLatas.setText(rs.getString("IdLoteLatas"));
        
           
           
           
           
           
       }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "NO MOSTRO EL ID DE LAS LATAS "+e.toString());
       }
           
         
        
                
                
                
    
    
    
    
}
    
    
    
    
